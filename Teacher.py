class Teacher:
    def setId(self,id):
        self.id=id
    def getId(self):
        return self.id
    
    def setName(self,name):
        self.name=name
    def getName(self):
        return self.name
    
    def setAddress(self,addr):
        self.addr=addr
    def getAddress(self):
        return self.addr
    
    def setSal(self,sal):
        self.sal=sal
    def getSal(self):
        return self.sal
        